async def init_sample_standards(db: Session):
    """初始化示例复核标准数据"""
    try:
        logger.info("开始初始化示例复核标准...")
        
        # 检查是否已有数据
        existing_count = db.query(ReviewStandard).count()
        if existing_count > 0:
            logger.info(f"数据库中已有 {existing_count} 条复核标准，跳过初始化")
            return
        
        # 创建检索服务
        retrieval_service = RetrievalService(db)
        
        # 插入示例数据
        created_count = 0
        for standard_data in SAMPLE_STANDARDS:
            try:
                standard = ReviewStandard(**standard_data)
                db.add(standard)
                db.commit()
                db.refresh(standard)
                
                # 创建向量索引
                await retrieval_service.add_standard_with_embedding(standard)
                
                created_count += 1
                logger.info(f"创建复核标准: {standard.title}")
                
            except Exception as e:
                db.rollback()
                logger.error(f"创建复核标准失败: {str(e)}")
        
        logger.info(f"示例复核标准初始化完成，共创建 {created_count} 条记录")
        
    except Exception as e:
        logger.error(f"初始化示例复核标准失败: {str(e)}")
        raise


async def main():
    """主函数"""
    db = SessionLocal()
    try:
        await init_sample_standards(db)
    finally:
        db.close()


if __name__ == "__main__":
    asyncio.run(main())